#Python offers lots of comparison features

'''
==, <, >, <=, >=, !=

== means the values are the same, however it uses 2 = signs since the = sign is reserved for defining variables

'''

#It also has math features

'''
+, -, /, *, **, //, %

* is for multiplication, ** is for exponents

// is for floor division, which gets the whole number value without remainder

% finds the remainder

'''

a = 5
b = 3
c = a+b
d = a-b
e = a*b
f = a/b
g = a**b
h = a//b
i = a%b

print(a, b, c, d, e, f, g, h, i)